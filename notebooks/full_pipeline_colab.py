# 1. VOLUME EXTRACTION FROM .npz FILES
#    Reads the same 'label' arrays used for training (dissertation §3.3.3)
# ======================================================================
def extract_mask_volumes(preproc_dir, dsc_scores):
    """
    Computes ground-truth mask volumes from the preprocessed .npz files.
    Each .npz contains 'image' and 'label' arrays at 1.0mm isotropic spacing
    (after resampling in preprocess.py), so voxel_vol = 1.0 mm^3.
    """
    npz_files = sorted([f for f in os.listdir(preproc_dir) if f.endswith('.npz')])

    if len(npz_files) == 0:
        print(f'ERROR: No .npz files found in {preproc_dir}')
        return []

    print(f'Found {len(npz_files)} .npz files in {preproc_dir}')
    results = []

    for i, fname in enumerate(npz_files):
        case_label = f'Case_{i+1:02d}'
        path = os.path.join(preproc_dir, fname)

        data = np.load(path)
        mask = data['label']  # ground-truth annotation

        if mask.ndim == 4:    # squeeze channel dim if present
            mask = mask[..., 0]

        # After preprocess.py resampling to 1.0mm isotropic:
        # voxel volume = 1.0 x 1.0 x 1.0 = 1.0 mm^3
        voxel_vol_mm3   = 1.0
        voxel_count     = int(np.sum(mask > 0))
        volume_mm3      = voxel_count * voxel_vol_mm3
        total_voxels    = mask.size
        imbalance_ratio = voxel_count / total_voxels

        results.append({
            'case':            case_label,
            'file':            fname,
            'shape':           mask.shape,
            'voxel_count':     voxel_count,
            'volume_mm3':      volume_mm3,
            'imbalance_ratio': imbalance_ratio,
            'dsc':             dsc_scores.get(case_label, None),
        })

        print(f'  {case_label} | {fname:<35} | '
              f'{voxel_count:>8,} voxels | {volume_mm3:>8.0f} mm^3')

    return results


# ======================================================================
# 2. STATISTICAL OUTLIER DETECTION (Tukey 1.5xIQR)
# ======================================================================
def detect_outliers(results):
    """
    Applies the Tukey 1.5xIQR criterion to identify anatomical outliers
    based on ground-truth mask volume, independent of model performance.
    """
    volumes = np.array([r['volume_mm3'] for r in results])

    mean_vol  = volumes.mean()
    std_vol   = volumes.std()
    q1, q3    = np.percentile(volumes, [25, 75])
    iqr       = q3 - q1
    lower_iqr = q1 - 1.5 * iqr
    upper_iqr = q3 + 1.5 * iqr

    stats = {
        'volumes':    volumes,
        'mean':       mean_vol,
        'std':        std_vol,
        'q1':         q1,
        'q3':         q3,
        'iqr':        iqr,
        'lower_iqr':  lower_iqr,
        'upper_iqr':  upper_iqr,
    }

    outlier_cases = []

    print('\n' + '=' * 68)
    print('  GROUND-TRUTH MASK VOLUME STATISTICS')
    print('=' * 68)
    print(f'  Mean      : {mean_vol:>10.1f} mm^3')
    print(f'  Std Dev   : {std_vol:>10.1f} mm^3')
    print(f'  Median    : {np.median(volumes):>10.1f} mm^3')
    print(f'  Q1 - Q3   : {q1:>10.1f} -- {q3:.1f} mm^3   (IQR = {iqr:.1f})')
    print(f'  1.5xIQR   : [{lower_iqr:.1f},  {upper_iqr:.1f}] mm^3')
    print()
    print(f'  {"Case":<10} {"Volume mm^3":>12} {"Voxels":>10} '
          f'{"DSC":>7}  Flag')
    print('  ' + '-' * 58)

    for r in results:
        v = r['volume_mm3']
        is_out = v < lower_iqr or v > upper_iqr
        flag = '<- OUTLIER' if is_out else ''
        dsc_str = f"{r['dsc']:.3f}" if r['dsc'] is not None else 'N/A'
        print(f"  {r['case']:<10} {v:>12.0f} {r['voxel_count']:>10,} "
              f"{dsc_str:>7}  {flag}")
        if is_out:
            outlier_cases.append(r['case'])

    print()
    if outlier_cases:
        print(f'  Outliers confirmed (1.5xIQR): {", ".join(outlier_cases)}')
        print('  Classification is INDEPENDENT of model performance.')
    else:
        print('  No volume outliers detected. Consider label-noise argument.')
    print('=' * 68)

    return stats, outlier_cases


