def sliding_window_inference(model, full_volume, patch_size=(96, 96, 96), stride=48):
    """
    Performs full-volume 3D segmentation using strided patches.
    Dynamically handles padding and channel squeezing to prevent boundary artifacts.
    """
    # -------------------------------------------------------------------
    # 1. DYNAMIC PADDING (Guarantee size >= patch_size + N*stride)
    # -------------------------------------------------------------------
    pad_width = []
    for i in range(3):
        dim_size = full_volume.shape[i]
        p_size = patch_size[i]

        if dim_size < p_size:
            # If volume is smaller than a single patch, pad it to exactly the patch size
            pad_amount = p_size - dim_size
        else:
            # If larger, ensure the remaining space is a clean multiple of the stride
            remainder = (dim_size - p_size) % stride
            pad_amount = 0 if remainder == 0 else stride - remainder

        # Pad only at the "end" (right/bottom/back) to keep coordinate math simple
        pad_width.append((0, pad_amount))

    padded = np.pad(full_volume, pad_width, mode='constant', constant_values=0)

    # -------------------------------------------------------------------
    # 2. PATCH EXTRACTION & PREDICTION
    # -------------------------------------------------------------------
    # Extract patches. Resulting shape: (n_z, n_y, n_x, p_z, p_y, p_x)
    patches = patchify(padded, patch_size, step=stride)

    # Flatten grid to a list of patches and add the channel dimension for the model
    # Shape becomes: (N, 96, 96, 96, 1)
    patches_reshaped = patches.reshape(-1, *patch_size, 1)

    # Predict using graph-optimized batching
    preds = model.predict(patches_reshaped, batch_size=8, verbose=0)

    # -------------------------------------------------------------------
    # 3. CHANNEL SQUEEZING & RECONSTRUCTION (unpatchify)
    # -------------------------------------------------------------------
    # Squeeze the channel dimension (-1) BEFORE unpatchifying to avoid dimension errors
    # Shape becomes: (N, 96, 96, 96)
    preds_squeezed = preds.squeeze(axis=-1)

    # Reshape back into the 6D grid format expected by unpatchify
    preds_grid = preds_squeezed.reshape(patches.shape)

    # Reassemble the padded 3D probability map
    prob_padded = unpatchify(preds_grid, padded.shape)

    # -------------------------------------------------------------------
    # 4. CROPPING & FINAL FORMATTING
    # -------------------------------------------------------------------
    # Crop the volume back to the exact original scanner dimensions
    slices = tuple(slice(0, orig_dim) for orig_dim in full_volume.shape)
    final_prob_map = prob_padded[slices]

    # Add the channel dimension back if your downstream pipeline expects (D, H, W, 1)
    final_prob_map = final_prob_map[..., np.newaxis]

    return final_prob_map

