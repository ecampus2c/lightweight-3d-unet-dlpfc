def build_lightweight_unet(input_shape=(96, 96, 96, 1)):
    def conv_block(x, filters):
        x = tf.keras.layers.Conv3D(filters, 3, padding='same', activation='relu')(x)
        x = tf.keras.layers.Conv3D(filters, 3, padding='same', activation='relu')(x)
        return x

    def encoder_block(x, filters):
        c = conv_block(x, filters)
        p = tf.keras.layers.MaxPooling3D((2,2,2))(c)
        return c, p

    def decoder_block(x, skip, filters):
        x = tf.keras.layers.Conv3DTranspose(filters, 2, strides=2, padding='same')(x)
        x = tf.keras.layers.Concatenate()([x, skip])
        x = conv_block(x, filters)
        return x

    inputs = tf.keras.layers.Input(shape=input_shape)

    # Encoder
    c1, p1 = encoder_block(inputs, 16)
    c2, p2 = encoder_block(p1, 32)
    c3, p3 = encoder_block(p2, 64)

    # Bottleneck
    b = conv_block(p3, 128)

    # Decoder
    d3 = decoder_block(b, c3, 64)
    d2 = decoder_block(d3, c2, 32)
    d1 = decoder_block(d2, c1, 16)

    # Output
    outputs = tf.keras.layers.Conv3D(1, 1, activation='sigmoid')(d1)

    return tf.keras.Model(inputs, outputs, name="Lightweight_3D_UNet")

# ====================== 2. LOSS FUNCTIONS (FIXED) ======================
def dice_coef(y_true, y_pred, smooth=1e-6):
    # FIX: Cast y_true to float32 to match y_pred
    y_true = tf.cast(y_true, tf.float32)

    y_true_f = tf.keras.backend.flatten(y_true)
    y_pred_f = tf.keras.backend.flatten(y_pred)
    intersection = tf.keras.backend.sum(y_true_f * y_pred_f)
    return (2. * intersection + smooth) / (tf.keras.backend.sum(y_true_f) + tf.keras.backend.sum(y_pred_f) + smooth)

def bce_dice_loss(y_true, y_pred):
    # Ensure y_true is float32 for BCE as well
    y_true = tf.cast(y_true, tf.float32)

    bce = tf.keras.losses.BinaryCrossentropy()(y_true, y_pred)
    return bce + (1.0 - dice_coef(y_true, y_pred))

