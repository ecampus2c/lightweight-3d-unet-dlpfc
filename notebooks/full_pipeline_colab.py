# 2. Helper Function: Isotropic Resampling
def resample_to_isotropic(image_path, is_mask=False):
    """
    Reads a NIfTI file with SimpleITK to preserve geometry,
    and resamples it to 1.0x1.0x1.0 mm isotropic spacing.
    """
    sitk_img = sitk.ReadImage(image_path)
    orig_spacing = sitk_img.GetSpacing()
    orig_size = sitk_img.GetSize()

    # Target spacing: 1mm isotropic
    target_spacing = (1.0, 1.0, 1.0)

    # Calculate new grid size to preserve physical field of view
    new_size = [
        int(np.round(orig_size[0] * (orig_spacing[0] / target_spacing[0]))),
        int(np.round(orig_size[1] * (orig_spacing[1] / target_spacing[1]))),
        int(np.round(orig_size[2] * (orig_spacing[2] / target_spacing[2])))
    ]

    resampler = sitk.ResampleImageFilter()
    resampler.SetSize(new_size)
    resampler.SetOutputSpacing(target_spacing)
    resampler.SetOutputDirection(sitk_img.GetDirection()) # Crucial for orientation
    resampler.SetOutputOrigin(sitk_img.GetOrigin())       # Crucial for alignment
    resampler.SetTransform(sitk.Transform())
    resampler.SetDefaultPixelValue(0)

    # Images need smooth B-Spline. Masks need sharp Nearest Neighbor (0 or 1 only)
    if is_mask:
        resampler.SetInterpolator(sitk.sitkNearestNeighbor)
    else:
        resampler.SetInterpolator(sitk.sitkBSpline)

    resampled_img = resampler.Execute(sitk_img)
    return sitk.GetArrayFromImage(resampled_img)

# 3. Helper Function: Center Crop or Pad
def center_crop_or_pad(volume, target_shape=(128, 128, 128)):
    """Ensures every tensor is exactly 128x128x128."""
    out = np.zeros(target_shape, dtype=volume.dtype)
    src_shape = np.array(volume.shape)
    tgt_shape = np.array(target_shape)

    # Calculate starting indices
    starts = np.maximum((src_shape - tgt_shape) // 2, 0)
    dst_starts = np.maximum((tgt_shape - src_shape) // 2, 0)

    # Copy overlapping region
    zs, ys, xs = starts
    zd, yd, xd = dst_starts
    cz, cy, cx = np.minimum(tgt_shape, src_shape)

    out[zd:zd+cz, yd:yd+cy, xd:xd+cx] = volume[zs:zs+cz, ys:ys+cy, xs:xs+cx]
    return out

