# 1. DEFINE THE REQUIRED FUNCTIONS (So Colab doesn't forget them)
# ======================================================================
def build_lightweight_unet(input_shape=(96, 96, 96, 1)):
    def conv_block(x, filters):
        x = layers.Conv3D(filters, 3, padding='same', activation='relu')(x)
        x = layers.Conv3D(filters, 3, padding='same', activation='relu')(x)
        return x
    def encoder_block(x, filters):
        c = conv_block(x, filters)
        p = layers.MaxPool3D((2, 2, 2))(c)
        return c, p
    def decoder_block(x, skip, filters):
        x = layers.Conv3DTranspose(filters, 2, strides=2, padding='same')(x)
        x = layers.Concatenate()([x, skip])
        x = conv_block(x, filters)
        return x

    inputs = layers.Input(shape=input_shape)
    c1, p1 = encoder_block(inputs, 16)
    c2, p2 = encoder_block(p1, 32)
    c3, p3 = encoder_block(p2, 64)
    b = conv_block(p3, 128)
    d3 = decoder_block(b, c3, 64)
    d2 = decoder_block(d3, c2, 32)
    d1 = decoder_block(d2, c1, 16)
    outputs = layers.Conv3D(1, 1, activation='sigmoid')(d1)

    return Model(inputs, outputs, name="Lightweight_3D_UNet")

def sliding_window_inference(model, full_volume, patch_size=(96, 96, 96), stride=48):
    pad_width = []
    for i in range(3):
        dim_size = full_volume.shape[i]
        p_size = patch_size[i]
        if dim_size < p_size:
            pad_amount = p_size - dim_size
        else:
            remainder = (dim_size - p_size) % stride
            pad_amount = 0 if remainder == 0 else stride - remainder
        pad_width.append((0, pad_amount))

    padded = np.pad(full_volume, pad_width, mode='constant', constant_values=0)
    patches = patchify(padded, patch_size, step=stride)
    patches_reshaped = patches.reshape(-1, *patch_size, 1)

    preds = model.predict(patches_reshaped, batch_size=8, verbose=0)
    preds_squeezed = preds.squeeze(axis=-1)
    preds_grid = preds_squeezed.reshape(patches.shape)

    prob_padded = unpatchify(preds_grid, padded.shape)
    slices = tuple(slice(0, orig_dim) for orig_dim in full_volume.shape)
    final_prob_map = prob_padded[slices]

    return final_prob_map

